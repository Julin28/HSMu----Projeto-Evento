//Responsável são nome completo, telefone para contato e valor por hora, mas, se
//for necessário, é possível incluir outras informações.

public class Responsavel {
	public String nome_completo;
	public String tel;
	public double valor_por_hora;
	
	public Responsavel(String nome_completo, String tel, double valor_por_hora) {
		super();
		this.nome_completo = nome_completo;
		this.tel = tel;
		this.valor_por_hora = valor_por_hora;
	}
	
	

	
}
